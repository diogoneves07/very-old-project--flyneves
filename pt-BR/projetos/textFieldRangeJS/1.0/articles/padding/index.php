<article class="important-content-section">
    <header>
        <h1>
            Propriedade padding
        </h1>
    </header>
    <p>
        Os preenchimento do elemento de entrada de texto
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
    Se você definir a propriedade de <strong>padding</strong>  CSS em seu elemento usando uma unidade de medida diferente de px, o valor das propriedades do objeto será a conversão do valor em px.
    </p>
    <h3 class="subtitles-main">
        <span>
            Propriedades do objeto:
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            top
        </span>
    </h4>
    <p>
        O preenchimento superior do elemento de entrada de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            left
        </span>
    </h4>
    <p>
        O preenchimento esquerdo do elemento de entrada de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            right
        </span>
    </h4>
    <p>
        O preenchimento direito do elemento de entrada de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            bottom
        </span>
    </h4>
    <p>
        O preenchimento inferior do elemento de entrada de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var padding = textFieldRange( element ).padding;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
