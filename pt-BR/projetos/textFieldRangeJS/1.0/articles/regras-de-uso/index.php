    <header>
        <h1>
            Regras de uso
        </h1>
    </header>
    <p>
    Conheça e compreenda as regras de uso da biblioteca textFieldRangeJS.
    </p>
    <h2 class="subtitles-main">
        <span>
        Suporte para elementos HTML
        </span>
    </h2>
    <p>
    Os recursos da biblioteca textFieldRangeJS estão disponíveis apenas para os elementos: <mark> &lt;input&gt; </mark> e <mark> &lt;textarea&gt; </mark>.    </p>
    <h2 class="subtitles-main">
        <span>
            Definindo linhas no campo de texto
        </span>
    </h2>
    <p>
    As linhas do campo de texto são definidas por quebras de linha ("\n" ou "\r\n") e pelo número de vezes que deve haver um estouro e não ouve, dando origem a uma nova linha.
    </p>


    <h2 class="subtitles-main">
        <span>
            Caracter \r
        </span>
    </h2>
    <p>Os métodos da biblioteca relacionados à posição e seleção, ignoram o caractere <mark>\r</mark>, para que a operação da biblioteca tenha o mesmo comportamento em todos os navegadores.</p>
    <h2 class="subtitles-main">
        <span>
        Desempenho 
        </span>
    </h2>
    <p>
    Para obter o melhor desempenho da biblioteca, não é recomendado exceder mais de <mark> 5000 caracteres </mark> ou <mark> 200 linhas </mark> em seu campo de texto.    </p>
