<article class="important-content-section">
    <header>
        <h1>
            Método breakLines()
        </h1>
    </header>
    <p>
        Obtém as linhas no campo de texto com caracteres de quebra de linha inseridos nos pontos específicos.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>

    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
            apply
        </span>
    </h4>
    <p>
        Este argumento é opcional. Se passado como <mark> true </mark>, este argumento indica que: o método deve inserir os caracteres de quebra de linha no campo de texto, apenas nos pontos específicos e necessários em que a quebra ocorre.
    </p>

    <p>
    Os “caracteres de quebra de linha” são: "\r\n" ou "\n" de acordo com o navegador.
    </p>
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
    O valor retornado é uma matriz.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var breakLines = textFieldRange( element ).breakLines();
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
