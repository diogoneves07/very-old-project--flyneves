<article class="important-content-section">
    <header>
        <h1>
            Propriedade offsetTop
        </h1>
    </header>
    <p>
    The current cursor displacement, including locomotion of the scroll relative to the upper limit of the text field of the display frame.    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Se você definir as propriedades CSS em seu elemento: (padding: x; ou border-width: x;), esse valor não será contado, pois a propriedade se refere apenas ao quadro do elemento onde o texto pode ser adicionado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            var offsetTop = textFieldRange( element ).offsetTop;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
