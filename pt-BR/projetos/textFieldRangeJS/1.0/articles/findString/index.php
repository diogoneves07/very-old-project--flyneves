<article class="important-content-section">
    <header>
        <h1>
            Método findString()
        </h1>
    </header>
    <p>
        
Encontre ocorrências de uma determinada sequência de caracteres no campo de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            string
        </span>
    </h4>
    <p>
        Este argumento é obrigatório. Especifica a sequência de caracteres a ser usada pelo método.
    </p>
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>

    <p>
    O valor retornado é uma matriz com objetos com propriedades para o resultado da pesquisa.
    </p>
    <h3 class="subtitles-main">
        <span>
            Propriedades do objeto:
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            start
        </span>
    </h4>
    <p>
        O início da sequência de caracteres no campo de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            end
        </span>
    </h4>
    <p>
        O final da sequência de caracteres no campo de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            string
        </span>
    </h4>
    <p>
        A seqüência de caracteres.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var findString = textFieldRange( element ).findString( "myString" );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
