<article class="important-content-section">
    <header>
        <h1>
            Propriedade selection
        </h1>
    </header>
    <p>
    A sequência de caracteres selecionada no campo de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
 

    <p>
    Se não houver seleção no campo de texto, o valor será uma sequência de caracteres vazia.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            var selection = textFieldRange( element ).selection;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
</article>
