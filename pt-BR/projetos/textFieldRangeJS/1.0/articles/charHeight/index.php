<article class="important-content-section">
    <header>
        <h1>
            Propriedade charHeight
        </h1>
    </header>
    <p>
    
A altura em px de todos os diferentes caracteres contidos no campo de texto..
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
    Ao contrário da largura, a altura dos caracteres no campo de texto é sempre o mesmo valor, embora visualmente alguns caracteres pareçam um pouco mais altos do que outros.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var charHeight = textFieldRange( element ).charHeight;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
</article>
