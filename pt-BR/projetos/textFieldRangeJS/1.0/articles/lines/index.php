<article class="important-content-section">
    <header>
        <h1>
            Método lines()
        </h1>
    </header>
    <p>
    Obtém todas as linhas contidas no campo de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Este método não espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
    Este método pode retornar com precisão uma matriz contendo as respectivas linhas no campo de texto em suas posições.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var lines = textFieldRange( element ).lines();
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
</article>
