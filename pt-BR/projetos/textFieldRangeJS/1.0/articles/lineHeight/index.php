<article class="important-content-section">
    <header>
        <h1>
            Propriedade lineHeight
        </h1>
    </header>
    <p>
    A altura atual em px das linhas contidas no campo de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
    Se você definir a propriedade de line-height CSS em seu elemento, usando uma unidade de medida diferente de px, o valor retornado será a conversão do valor para px.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var lineHeight = textFieldRange( element ).lineHeight;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
</article>
