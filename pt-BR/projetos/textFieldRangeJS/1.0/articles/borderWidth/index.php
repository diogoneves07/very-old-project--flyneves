<article class="important-content-section">
    <header>
        <h1>
            Propriedade borderWidth
        </h1>
    </header>
    <p>
    A largura em px das bordas do elemento de entrada de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
    Se você definir a propriedade CSS border-width em seu elemento, usando uma unidade de medida diferente de px, O valor das propriedades do objeto será a conversão do valor para px.
    </p>
    <h3 class="subtitles-main">
        <span>
            Propriedades do objeto:
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            top
        </span>
    </h4>
    <p>
        A largura em px da borda superior do elemento de entrada de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            left
        </span>
    </h4>
    <p>
        A largura em px da borda esquerda do elemento de entrada de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            right
        </span>
    </h4>
    <p>
        A largura em px da borda direita do elemento de entrada de texto.
    </p>
    <h4 class="subtitle-marked">
        <span>
            bottom
        </span>
    </h4>
    <p>
        A largura em px da borda inferior do elemento de entrada de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var borderWidth = textFieldRange( element ).borderWidth;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
