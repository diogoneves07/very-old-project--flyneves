<article class="important-content-section">
    <header>
        <h1>
            Método replaceSelection()
        </h1>
    </header>
    <p>
        Substitui a sequência de caracteres selecionada no campo de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>

    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
            string
        </span>
    </h4>
    <p>
        Este argumento é obrigatório. Especifica a sequência de caracteres a ser usada pelo método.
    </p>
    <h4 class="subtitle-marked">
        <span>
            select
        </span>
    </h4>
    <p>Este argumento é opcional. Especifica se a nova string deve ser selecionada.</p>

    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
        O valor retornado é o objeto textFieldRange atualizado.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            textFieldRange( element ).replaceSelection( "string" );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
