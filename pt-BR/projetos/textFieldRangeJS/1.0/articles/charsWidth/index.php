<article class="important-content-section">
    <header>
        <h1>
            Propriedade charsWidth
        </h1>
    </header>
    <p>
    A largura em px de todos os diferentes caracteres contidos no campo de texto.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
        Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
    Contém objetos com propriedades relacionadas aos caracteres.
    </p>
    <h3 class="subtitles-main">
        <span>
            Propriedades do objeto:
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            char
        </span>
    </h4>
    <p>
        O caracter.
    </p>
    <h4 class="subtitle-marked">
        <span>
            width
        </span>
    </h4>
    <p>
    A largura em px do caracter.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var charsWidth =textFieldRange( element ).charsWidth;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
