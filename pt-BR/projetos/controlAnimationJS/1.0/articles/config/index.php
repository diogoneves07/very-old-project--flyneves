<article class="important-content-section">
        <header>
            <h1>
                Método config()
            </h1>
        </header>
            <p>
            Define os valores padrão para as propriedades dos objetos de animação.
            </p>
            <h2 class="subtitles-main">
                <span>
                    Detalhe
                </span>
            </h2>
            <p>
            Este método pertence ao objeto controlAnimation.
            </p>
            <h2 class="subtitles-main">
                <span>
                    Descrição
                </span>
            </h2>
            <p>
                Este método não espera argumentos.
            </p>
            <h3 class="subtitles-main">
                <span>
                    Valor de retorno
                </span>
            </h3>
            <p>
            O valor retornado é um objeto. Se houver alteração em alguma de suas propriedades, ao iniciar o novo objeto de animação ele já terá os novos valores padrão.
            </p>
            <h2 class="subtitles-main">
                <span>
                    Syntax
                </span>
            </h2>
            <div class="code-example">
                <pre><code>
                    var animationObjectsConfig = controlAnimation.config();
                </code></pre>
            </div>
            <h2 class="subtitles-main">
                <span>
                    Suporte a navegadores
                </span>
            </h2>
            <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
        </article>

