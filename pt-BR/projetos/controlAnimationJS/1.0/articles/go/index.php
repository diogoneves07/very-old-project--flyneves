<article class="important-content-section">
    <header>
        <h1>
            Método go()
        </h1>
    </header>
    <p>
    Faz com que a interação da animação retorne a uma determinada posição e, após o retorno ser concluído, ela termina.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
    Este método pertence ao objeto da animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
        goTo
        </span>
    </h4>
    <p>
    Este argumento é obrigatório. Especifica a posição para a qual a animação deve retornar.
    </p>
    <p class="text-expected-type-of-data">
    Tipo esperado:<strong> number.</strong>
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            myAnimationObject.go( goTo );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
