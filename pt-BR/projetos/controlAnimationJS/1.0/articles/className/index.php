<article class="important-content-section">
    <header>
        <h1>
            Propriedade className
        </h1>
    </header>
        <p>
        Define o nome da classe CSS, a ser vinculada ao objeto de animação.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detalhe
            </span>
        </h2>
        <p>
            Esta propriedade é de leitura e gravação.
        </p>
       
        <p>
        Esta propriedade pertence ao objeto de animação, não ao objeto controlAnimation.
        </p>
        <p class="text-expected-type-of-data">
        Tipo esperado:<strong> string</strong>.
        </p>
        <h2 class="subtitles-main">
            <span>
                Descrição
            </span>
        </h2>
        <p>
        Ao inserir o estilo em quadros-chave e definir a propriedade <a href="../insertStyle/"> insertStyle </a> como <mark> auto </mark> ou <mark> tag </mark>, você precisará de um nome para a classe que será adicionada aos elementos, entretanto, a própria biblioteca já define um valor inicial para esta propriedade que pode ser mantido.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject.className = myFunction;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Suporte a navegadores
            </span>
        </h2>
        <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
