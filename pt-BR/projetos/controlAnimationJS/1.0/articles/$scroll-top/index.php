        Define o deslocamento da rolagem na direção superior.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detalhe
            </span>
        </h2>
        <p>
            Esta propriedade é de leitura e gravação.
        </p>
        
        <p>
     Esta é uma propriedade de estilo especial criada para controlAnimation.
     </p>
        <p class="text-expected-type-of-data">
        Tipo esperado:<strong> number</strong>.
        </p>
        <h2 class="subtitles-main">
            <span>
                Descrição
            </span>
        </h2>
        <p>
        Esta propriedade só deve ser usada no estilo CSS dos keyframes.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject[100] = "$scroll-top: 100; top: 50px; left...";
            </code></pre>
        </div>
