<article class="important-content-section">
<header>
    <h1>
        Método clone()
    </h1>
</header>
    <p>
    Clona o objeto de animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
    Este método pertence ao objeto da animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>  
    </h2>
    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
        amount
        </span>
    </h4>
    <p class="text-expected-type-of-data">
    Tipo esperado:<strong> number || object</strong>.
    </p>
    <p>
    Este argumento é opcional. Especifica o número de clones.
    </p>
    <p>
    Você também pode passar um array com elementos HTML. Um objeto será criado para cada elemento da matriz e a <a href="../element/"> propriedade element </a> será definida com o elemento.
    </p>
    <p>
    O valor padrão é: <mark>1</mark>.
    </p>
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
    O valor retornado é uma matriz com clones do objeto de animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
           var myClones = myAnimationObject.clone( amount );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p></article>

