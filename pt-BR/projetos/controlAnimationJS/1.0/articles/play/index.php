<article class="important-content-section">
<header>
    <h1>
        Método play()
    </h1>
</header>
    <p>
        Executa a nova animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
    Este método pertence ao objeto controlAnimation.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
        object
        </span>
    </h4>
    <p>
        Este argumento é obrigatório. Específica o objeto da animação.
    </p>
    <p class="text-expected-type-of-data">
    Tipo esperado:<strong> object</strong>.
    </p>
    <p>
    O objeto a ser passado também pode ser uma matriz contendo objetos de animação.
    </p>
    <h4 class="subtitle-marked">
        <span>
        all
        </span>
    </h4>
    <p>
        This argument is optional. When passing multiple animation objects with identical styles, pass <mark> true </mark> to improve performance.
    </p>
    <p class="text-expected-type-of-data">
    Tipo esperado:<strong> boolean</strong>.
    </p>
     
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
        Nenhum valor é retornado.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            controlAnimation.play( object );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
