<article class="important-content-section">
    <header>
        <h1>
            Método cancel()
        </h1>
    </header>
        <p>
        Cancela a execução da animação.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detalhe
            </span>
        </h2>
        <p>
        Este método pertence ao objeto da animação.
        </p>
        <h2 class="subtitles-main">
            <span>
                Descrição
            </span>
        </h2>
        <p>
            Este método não espera argumentos.
        </p>
        <p>
        Ao contrário do <a href="../pause/"> método pause </a>, este método apaga os dados da animação desnecessários depois de cancelada.
        </p>
        <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
        Nenhum valor é retornado.
    </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
                myAnimationObject.cancel();
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Suporte a navegadores
            </span>
        </h2>
        <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
