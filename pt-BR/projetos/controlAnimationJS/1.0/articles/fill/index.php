<article class="important-content-section">
    <header>
        <h1>
            Propriedade fill
        </h1>
    </header>
        <p>
        Define quais valores são aplicados pela animação, quando chega o fim de seus ciclos.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detalhe
            </span>
        </h2>
        <p>
            Esta propriedade é de leitura e gravação.
        </p>
        <p>
        Esta propriedade pertence ao objeto de animação, não ao objeto controlAnimation.
        </p>
        <p class="text-expected-type-of-data">
        Tipo esperado:<strong> boolean || number</strong>.
        </p>
        
        <h2 class="subtitles-main">
            <span>
                Descrição
            </span>
        </h2>
        
        <p>
        Valores aceitos para esta propriedade:
        </p>
        <ul class="normal-list">
            <li><mark>true:</mark> Os últimos valores são mantidos.</li>
            <li><mark>false:</mark> Nenhum valor é mantido.</li>
            <li><mark>number:</mark> A animação é executada novamente até atingir o valor definido.</li>
        </ul>
        <p>
            O valor padrão é: <mark>false</mark>.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject.fill  = true;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Suporte a navegadores
            </span>
        </h2>
        <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
