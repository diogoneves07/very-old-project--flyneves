<article class="important-content-section">
    <header>
        <h1>
            Propriedade state
        </h1>
    </header>
        <p>
        O estado atual em que a animação está.        </p>
        <h2 class="subtitles-main">
            <span>
                Detalhe
            </span>
        </h2>
        <p>
            Esta é uma propriedade somente leitura, ou seja, seu valor não deve ser alterado.
        </p>
        <p>
        Esta propriedade pertence ao objeto de animação, não ao objeto controlAnimation.
        </p>
        <h2 class="subtitles-main">
            <span>
                Descrição
            </span>
        </h2>
        <p>
        Valores aceitos para esta propriedade:
        </p>
        <ul class="normal-list">
            <li><mark>running:</mark> A animação está em execução.</li>
            <li><mark>paused:</mark> A animação está em pausada.</li>
            <li><mark>completed:</mark> Todos os ciclos da animação foram concluídos.</li>
            <li><mark>canceled:</mark> A animação foi cancelada.</li>
        </ul>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
           var state = myAnimationObject.state;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Suporte a navegadores
            </span>
        </h2>
        <p>Esta propriedade possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
</article>
