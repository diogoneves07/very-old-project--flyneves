<article class="important-content-section">
<header>
    <h1>
        Método get()
    </h1>
</header>
    <p>
    Obtém um objeto de animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
    Este método pertence ao objeto controlAnimation.
    </p>
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Este método espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Argumentos
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
            name
        </span>
    </h4>
    <p>
        Este argumento é obrigatório. Especifica o identificador de animação do objeto.
    </p>
    <p class="text-expected-type-of-data">
    Tipo esperado:<strong> string || number</strong>.
    </p>
    <p>
    <blockquote class = "important-note">
        O identificador da animação é o valor da <a href="../name/">propriedade name</a>.
    </blockquote>
    </p>
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
    O valor retornado é o objeto em caso de sucesso ou <mark> false </mark> em caso de falha.
    </p>
    
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            controlAnimation.get( name );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
    <p>Este método possui a mesma tabela de compatibilidade da biblioteca, ou seja, basta olhar a tabela de suporte da biblioteca.</p>
