    <h1>
        Método getAll()
    </h1>
</header>
    <p>
    Obtém todos os objetos de animação.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detalhe
        </span>
    </h2>
    <p>
    Este método pertence ao objeto controlAnimation.
    </p>
    
    <h2 class="subtitles-main">
        <span>
            Descrição
        </span>
    </h2>
    <p>
        Este método não espera argumentos.
    </p>
    <h3 class="subtitles-main">
        <span>
            Valor de retorno
        </span>
    </h3>
    <p>
    O valor retornado são os objetos em uma matriz em caso de sucesso ou <mark> false </mark> em caso de falha.    </p>
    
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            controlAnimation.getAll();
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Suporte a navegadores
        </span>
    </h2>
