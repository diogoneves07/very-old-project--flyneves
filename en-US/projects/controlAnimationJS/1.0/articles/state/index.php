<article class="important-content-section">
    <header>
        <h1>
             state property
        </h1>
    </header>
        <p>
        The current state the animation is in.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
            This is a read-only property, that is, its value must not be changed.
        </p>
        <p>
        This property belongs to the animation object, not the controlAnimation object.
        </p>
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
        Accepted values for this property:
        </p>
        <ul class="normal-list">
            <li><mark>running:</mark> The animation is running.</li>
            <li><mark>paused:</mark> The animation is paused.</li>
            <li><mark>completed:</mark> All animation cycles have been completed.</li>
            <li><mark>canceled:</mark> The animation has been canceled.</li>
        </ul>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
           var state = myAnimationObject.state;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
