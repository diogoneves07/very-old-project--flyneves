<article class="important-content-section">
    <header>
        <h1>
            cancelAll() Method        
        </h1>
    </header>
        <p>
        Cancels the execution of all animations.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
        This method belongs to the controlAnimation object.
        </p>
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
            This method does not expect any arguments.
        </p>
        <p>
        Unlike the <a href="../pauseAll/"> pauseAll method </a>, this method erases unnecessary animation data after it is canceled.
        </p>
        <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
        No value is returned.
    </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            controlAnimation.cancelAll();
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
