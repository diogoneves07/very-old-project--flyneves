<article class="important-content-section">
    <header>
        <h1>
            $scroll-to property
        </h1>
    </header>
        <p>
        Sets the scroll offset in the top and left directions.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
            This property is read and write.
        </p>
        
        <p>
        This is a special style property, created for controlAnimation.
        </p>
        <p class="text-expected-type-of-data">
        Expected type:<strong> number</strong>.
        </p>
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
        This property should only be used in the CSS style of the keyframes.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject[100] = "$scroll-to: 100 200; top: 50px; left...";
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
