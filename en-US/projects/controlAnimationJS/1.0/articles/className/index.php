<article class="important-content-section">
    <header>
        <h1>
        className property
        </h1>
    </header>
        <p>
        Defines the name of the CSS class, to be linked to the animation object.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
            This property is read and write.
        </p>
       
        <p>
        This property belongs to the animation object, not the controlAnimation object.
        </p>
        <p class="text-expected-type-of-data">
        Expected type:<strong> string</strong>.
        </p>
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
        When inserting the style in keyframes and setting the <a href="../insertStyle/"> insertStyle </a> property to <mark> auto </mark> or <mark> tag </mark>, you will need of a name for the class that will be added to the elements, however, the library itself already defines an initial value for this property that can be maintained.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject.className = myFunction;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
