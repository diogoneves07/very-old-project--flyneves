<article class="important-content-section">
    <header>
        <h1>
             name property
        </h1>
    </header>
        <p>
        Sets the identifier for the animation object.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
            This property is read and write.
        </p>
        
        <p>
        This property belongs to the animation object, not the controlAnimation object.
        </p>
        <p class="text-expected-type-of-data">
        Expected type:<strong> string || number</strong>.
        </p>
        
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
        If you do not define the value of this property, it will be defined with the value of the <a href="../animationId/"> animationId property </a>.
            </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject.name = "myAnimation";
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
