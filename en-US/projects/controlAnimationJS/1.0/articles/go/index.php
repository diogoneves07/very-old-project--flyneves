<article class="important-content-section">
    <header>
        <h1>
        go() method
        </h1>
    </header>
    <p>
    Causes the animation interaction to return to a certain position and, after the return is complete, it ends.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
    This method belongs to the animation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method expect arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Arguments
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
        goTo
        </span>
    </h4>
    <p>
    This argument is mandatory. Specifies the position to which the animation should return.
    </p>
    <p class="text-expected-type-of-data">
    Expected type:<strong> number.</strong>
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            myAnimationObject.go( goTo );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
