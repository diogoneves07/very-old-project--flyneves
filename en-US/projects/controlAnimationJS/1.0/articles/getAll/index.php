    getAll () Method
    </h1>
</header>
    <p>
    Gets all animation objects.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
    This method belongs to the controlAnimation object.
    </p>
    
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method does not expect any arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
    The returned value is the objects in an array on success or <mark> false </mark> on failure.
    </p>
    
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
