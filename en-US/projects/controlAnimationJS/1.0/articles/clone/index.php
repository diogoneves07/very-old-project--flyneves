<article class="important-content-section">
<header>
    <h1>
    clone() method
    </h1>
</header>
    <p>
    Clones the animation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
    This method belongs to the animation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method expect arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Arguments
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
        amount
        </span>
    </h4>
    <p class="text-expected-type-of-data">
    Expected type:<strong> number || object</strong>.
    </p>
    <p>
    This argument is optional. Specifies the number of clones.
    </p>
    <p>
    You can also pass an array with HTML elements. An object will be created for each element of the array and the <a href="../element/"> element property </a> will be defined with the element.
    </p>
    <p>
    The default value is: <mark>1</mark>.
    </p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
    The returned value is an array with clones of the animation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
           var myClones = myAnimationObject.clone( amount );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>

