<article class="important-content-section">
<header>
    <h1>
         config() method
    </h1>
</header>
    <p>
    Sets the default values for the properties of animation objects.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
    This method belongs to the controlAnimation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method does not expect any arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
    The returned value is an object. If there is a change in any of its properties, when starting the new animation object it will already have the new default values.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            var animationObjectsConfig = controlAnimation.config();
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>

