<article class="important-content-section">
    <header>
        <h1>
        listenerFired property
        </h1>
    </header>
        <p>
        The listener name which was triggered.
        </p>
        <h2 class="subtitles-main">
            <span>
            Detail
            </span>
        </h2>
        <p>
        This is a read-only property, that is, its value must not be changed.
        </p>       
        <p>
        This property belongs to the animation object, not the controlAnimation object.
        </p> 
        <h2 class="subtitles-main">
            <span>
            Description
            </span>
        </h2>
        <p>
        This property can be used to identify the newly triggered listener.        </p>
        <p>
        The default value is: <mark>false</mark>,indicating that no listener has been triggered.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            var listenerFired = myAnimationObject.listenerFired;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
            Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
