<article class="important-content-section">
<header>
    <h1>
    play() method
    </h1>
</header>
    <p>
    Play the new animation.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
    This method belongs to the controlAnimation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method expect arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Arguments
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
        object
        </span>
    </h4>
    <p>
        This argument is mandatory. Específica o objeto da animação.
    </p>
    <p class="text-expected-type-of-data">
    Expected type:<strong> object</strong>.
    </p>
    <p>
    The object to be passed can also be an array containing animation objects.
    </p>
    <h4 class="subtitle-marked">
        <span>
        all
        </span>
    </h4>
    <p>
    This argument is optional. When passing multiple animation objects with identical styles, pass <mark> true </mark> to improve performance.
    </p>
    <p class="text-expected-type-of-data">
    Expected type:<strong> boolean</strong>.
    </p>
     
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
        No value is returned.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            controlAnimation.play( object );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
