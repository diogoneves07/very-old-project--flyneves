<article class="important-content-section">
<header>
    <h1>
        get() method
    </h1>
</header>
    <p>
    Gets an animation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
    This method belongs to the controlAnimation object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method expect arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Arguments
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
            name
        </span>
    </h4>
    <p>
        This argument is mandatory. Specifies the object's animation identifier.
    </p>
    <p class="text-expected-type-of-data">
    Expected type:<strong> string || number</strong>.
    </p>
    <p>
    <blockquote class = "important-note">
    The animation identifier is the value of the <a href="../name/"> name property </a>.
    </blockquote>
    </p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
    The returned value is the object on success or <mark> false </mark> on failure.            </p>
    
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            controlAnimation.get( name );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
