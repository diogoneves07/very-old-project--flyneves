<article class="important-content-section">
    <header>
        <h1>
        animationId Property
        </h1>
    </header>
        <p>
        The unique identifier for each animation object.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
            This is a read-only property, that is, its value must not be changed.
        </p>       
        <p>
        This property belongs to the animation object, not the controlAnimation object.
        </p> 
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
        This property is very important, as it is used by the library to identify each of the animation objects.
        </p>
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            var animationId = myAnimationObject.animationId;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
