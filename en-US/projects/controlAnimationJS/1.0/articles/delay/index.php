<article class="important-content-section">
    <header>
        <h1>
        delay property
        </h1>
    </header>
        <p>
        Sets the delay between each animation interaction.
        </p>
        <h2 class="subtitles-main">
            <span>
                Detail
            </span>
        </h2>
        <p>
            This property is read and write.
        </p>
        <p>
        This property belongs to the animation object, not the controlAnimation object.
        </p>
        <p class="text-expected-type-of-data">
        Expected type:<strong> number || boolean || undefined</strong>.
        </p>
        
        <h2 class="subtitles-main">
            <span>
                Description
            </span>
        </h2>
        <p>
        Accepted values for this property:
        </p>
        <ul class="normal-list">
            <li><mark>number:</mark> Sets the delay in seconds.</li>
            <li><mark>false:</mark> There is no delay.</li>
            <li><mark>undefined:</mark> Não há atraso.</li>
        </ul>
        <p>
            The default value is: <mark>false</mark>.
        </p>
        
        <h2 class="subtitles-main">
            <span>
                Syntax
            </span>
        </h2>
        <div class="code-example">
            <pre><code>
            myAnimationObject.delay = 5;
            </code></pre>
        </div>
        <h2 class="subtitles-main">
            <span>
                Browser Support
            </span>
        </h2>
        <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
