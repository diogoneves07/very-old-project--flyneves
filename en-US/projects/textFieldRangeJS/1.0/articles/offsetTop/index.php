<article class="important-content-section">
    <header>
        <h1>
            offsetTop property
        </h1>
    </header>
    <p>
    O deslocamento atual do cursor, incluindo a locomoção da rolagem em relação ao limite superior do quadro de exibição do campo de texto.    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        The current cursor displacement, including locomotion of the scroll in relation to the left delimiter of the frame display text field.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            var offsetTop = textFieldRange( element ).offsetTop;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
