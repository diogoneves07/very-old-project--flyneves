<article class="important-content-section">
    <header>
        <h1>
             charsWidth property
        </h1>
    </header>
    <p>
    The width in px of all the different characters contained in the text field.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
    Contains objects with properties related to the characters.
    </p>
    <h3 class="subtitles-main">
        <span>
            Object properties:
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            char
        </span>
    </h4>
    <p>
    The character.
    </p>
    <h4 class="subtitle-marked">
        <span>
            width
        </span>
    </h4>
    <p>
    The width in px of the character.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var charsWidth =textFieldRange( element ).charsWidth;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
