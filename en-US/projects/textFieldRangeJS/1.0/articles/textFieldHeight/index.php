<article class="important-content-section">
    <header>
        <h1>
             textFieldHeight property
        </h1>
    </header>
    <p>
    The height of the text entry field.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
    The text entry field is the frame of the element where the text can be added.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var textFieldHeight = textFieldRange( element ).textFieldHeight;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
