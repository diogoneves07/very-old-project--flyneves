<article class="important-content-section">
    <header>
        <h1>
             currentLineText property
        </h1>
    </header>
    <p>
    The character string of the line in the text field where the caret is positioned.  </p>
    <h2 class="subtitles-main">
        <span>
        Description
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main" id="Syntax">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            var currentLineText = textFieldRange( element ).currentLineText;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
