<article class="important-content-section">
    <header>
        <h1>
            lineHeight property       
        </h1>
    </header>
    <p>
    The current height in px of the lines contained in the text field.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
    If you define the CSS line-height property in your element, using a unit of measurement other than px, the value returned will be the conversion of the value to px.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var lineHeight = textFieldRange( element ).lineHeight;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
