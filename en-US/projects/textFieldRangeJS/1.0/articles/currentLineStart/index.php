<article class="important-content-section">
    <header>
        <h1>
             currentLineStart property
        </h1>
    </header>
    <p>
    The position of the first character in the line of the text field where the caret is positioned.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
    An integer indicating the position of the first character on the line.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            var currentLineStart = textFieldRange( element ).currentLineStart;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
