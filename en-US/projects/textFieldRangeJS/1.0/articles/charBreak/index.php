<article class="important-content-section">
    <header>
        <h1>
             charBreak property
        </h1>
    </header>
    <p>
    The character that the browser uses to break lines in the text field.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
    The “line break character” are: "\r\ n" or "\n" according to the browser.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            textFieldRange( element ).charBreak;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
