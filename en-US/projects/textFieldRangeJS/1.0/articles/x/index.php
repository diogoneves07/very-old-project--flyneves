<article class="important-content-section">
    <header>
        <h1>
             x property
        </h1>
    </header>
    <p>
    The current offset of the caret in relation to the left delimiter of the text field display frame.
    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        If you define the CSS properties in your element: (padding: x; or border-width: x;), this value is not counted, because the property refers only to the element's frame where text is allowed to be added.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var x = textFieldRange( element ).x;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
