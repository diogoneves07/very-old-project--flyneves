<article class="important-content-section">
    <header>
        <h1>
             charHeight property
        </h1>
    </header>
    <p>
    The height in px of all the different characters contained in the text field.    </p>
    <h2 class="subtitles-main">
        <span>
            Detail
        </span>
    </h2>
    <p>
        This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
    Unlike the width, the height of the characters in the text field is always the same value, although visually some characters look a little taller than others.
    </p>

    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var charHeight = textFieldRange( element ).charHeight;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
</article>
