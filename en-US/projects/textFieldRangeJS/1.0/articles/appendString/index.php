<header>
    <h1>
       appendString() method
    </h1>
</header>
    <p>
    Inserts a string at a given position in the text field.    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method expect arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Arguments
        </span>
    </h3>
    <h4 class="subtitle-marked">
        <span>
            position
        </span>
    </h4>
    <p>
        This argument is mandatory. Specifies the position where the string is to be inserted.
    </p>
    <h4 class="subtitle-marked">
        <span>
            string
        </span>
    </h4>
    <p>
        This argument is mandatory. Specifies the string to be used by the method.
    </p>

    <h4 class="subtitle-marked">
        <span>
            select
        </span>
    </h4>
    <p>This argument is optional. Specifies whether to select the new string.</p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
    The returned value is the updated textFieldRange object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        textFieldRange( element ).appendString( 0, "string" );
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
