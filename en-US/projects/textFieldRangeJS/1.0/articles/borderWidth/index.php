<article class="important-content-section">
    <header>
        <h1>
        borderWidth property
        </h1>
    </header>
    <p>
    The width in px of the edges of the text input element.
    </p>
    <h2 class="subtitles-main">
        <span>
        Detail
        </span>
    </h2>
    <p>
    This is a read-only property, that is, its value must not be changed.
    </p>
    <h2 class="subtitles-main">
        <span>
        Description
        </span>
    </h2>
    <p>
    If you set the CSS border-width property on your element, using a unit of measurement other than px, The value of the object's properties will be the conversion of the value to px.
    </p>
    <h3 class="subtitles-main">
        <span>
        Object properties:
        </span>
    </h3>

    <h4 class="subtitle-marked">
        <span>
            top
        </span>
    </h4>
    <p>
    The width in px of the top border of the text entry element.
    </p>
    <h4 class="subtitle-marked">
        <span>
            left
        </span>
    </h4>
    <p>
    The width in px of the left border of the text entry element.
    </p>
    <h4 class="subtitle-marked">
        <span>
            right
        </span>
    </h4>
    <p>
    The width in px of the right border of the text entry element.
    </p>
    <h4 class="subtitle-marked">
        <span>
            bottom
        </span>
    </h4>
    <p>
    The px width of the bottom border of the text entry element.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var borderWidth = textFieldRange( element ).borderWidth;
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
        Browser Support
        </span>
    </h2>
    <p>This property has the same compatibility table as the library, that is, just look at the library support table.</p>
