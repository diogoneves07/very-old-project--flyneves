<article class="important-content-section">
    <header>
        <h1>
             lines() method
        </h1>
    </header>
    <p>
    Gets all lines contained in the text field.
    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method does not expect any arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
    This method can accurately return an array containing the respective lines in the text field at their positions.    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
        var lines = textFieldRange( element ).lines();
        </code></pre>
    </div>
    <h2 class="subtitles-main">
        <span>
            Browser Support
        </span>
    </h2>
    <p>This method has the same compatibility table as the library, that is, just look at the library support table.</p>
