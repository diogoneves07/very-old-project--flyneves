             refresh() method
        </h1>
    </header>
    <p>
    Updates the object's property values.    </p>
    <h2 class="subtitles-main">
        <span>
            Description
        </span>
    </h2>
    <p>
        This method does not expect any arguments.
    </p>
    <h3 class="subtitles-main">
        <span>
            Return value
        </span>
    </h3>
    <p>
        The returned value is the updated textFieldRange object.
    </p>
    <h2 class="subtitles-main">
        <span>
            Syntax
        </span>
    </h2>
    <div class="code-example">
        <pre><code>
            textFieldRange( element ).refresh();
        </code></pre>
    </div>
