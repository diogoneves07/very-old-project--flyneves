    <header>
        <h1>
        Rules of use
        </h1>
    </header>
    <p>
    Know and understand the rules for using the textFieldRangeJS library.
    </p>
    <h2 class="subtitles-main">
        <span>
        Support for HTML elements
        </span>
    </h2>
    <p>
    The features of the textFieldRangeJS library are only available for the elements: <mark>&lt;input&gt;</mark> and <mark> &lt;textarea&gt; </mark>.    </p>
    <h2 class="subtitles-main">
        <span>
        Defining lines in the text field
        </span>
    </h2>
    <p>
    The lines in the text field are defined by line breaks ("\n" or "\r\n") and by the number of times there should be an overflow and not hear, giving rise to a new line.
    </p>
   
    <h2 class="subtitles-main">
        <span>
        Character \r
        </span>
    </h2>
    <p>The library's methods related to position and selection, ignore the <mark>\r</mark> character, so the operation of the library will have the same behavior in all browsers.</p>
    <h2 class="subtitles-main">
        <span>
        Performance 
        </span>
    </h2>
    <p>
    For best library performance, it is not recommended to exceed more than <mark> 5000 characters </mark> or <mark> 200 lines </mark> in your text field.
    </p>
</article>

